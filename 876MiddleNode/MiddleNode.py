# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None

class Solution:
    def middleNode(self, head: ListNode) -> ListNode:
        '''
        '''
        Node_list = []
        i = 0
        Node_list.append(head)
        while True:
            if not Node_list[i].next:
                break
            Node_list.append(Node_list[i].next)
            i+=1
        return Node_list[len(Node_list)//2]


