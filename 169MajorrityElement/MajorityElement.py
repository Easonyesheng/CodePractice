from math import floor

class Solution:
    def majorityElement(self, nums) -> int:
        elem_nums = set(nums)
        N = len(nums)
        N_half = floor(N/2)

        for i in elem_nums:
            if nums.count(i) > N_half:
                return i

if __name__ == "__main__":
    s = Solution()
    nums  = [2,2,1,1,1,2,2]
    print(s.majorityElement(nums))

